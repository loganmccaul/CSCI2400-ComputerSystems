/* 
 * anyEvenBit - return 1 if any even-numbered bit in word set to 1
 *   Examples anyEvenBit(0xA) = 0, anyEvenBit(0xE) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int anyEvenBit(int x) {
	//Create a temporary variable that is unsigned
	//Then shift over to to the first multiple of 2 and or it so
	//if it is even you get 2 even bits, then double it and repeat
	//Shift it all the way left then back to so if there is a 1
	//it would be in the first bit
	unsigned tmp = x;
	tmp = tmp | (tmp >> 2);
	tmp = tmp | (tmp >> 4);
	tmp = tmp | (tmp >> 8);
	tmp = tmp | (tmp >> 16);
	tmp = tmp << 31;
	return tmp >> 31;

}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
	//shift m and n by 3 so that it is equivalent to hex
	//then shift x by m and or it with x by n and add 255
	//shift back m and n to have the two numbers in the right place
	//and or x with tmp to get the two numbers switched
    m = m << 3;
    n = n << 3;
    unsigned int tmp = ((x >> m) ^ (x >> n)) & 255;
    tmp = (tmp << m) | (tmp << n);
    return (x ^ tmp);
}
